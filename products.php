function renderVS(){
    const wrap = document.getElementById("vsCarousel");
    const navs = wrap.querySelectorAll(".vs-nav");
    wrap.innerHTML="";
    navs.forEach(n=>wrap.appendChild(n));

    const prev = vsIndex===0 ? vsData.length-1 : vsIndex-1;
