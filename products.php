        </div>

            <img src="${vsData[vsIndex].models[vsModel].image}">
        </div>

        <div class="vs-item vs-next" onclick="vsNext()">
            <img src="${vsData[next].models[0].image}">
        </div>
    `;

    document.getElementById("vsName").innerText = vsData[vsIndex].name;
    document.getElementById("vsCategory").innerText = vsData[vsIndex].category;
    document.getElementById("vsSpecs").href = vsData[vsIndex].models[vsModel].specs;

    const models = document.getElementById("vsModels");
    models.innerHTML="";
    vsData[vsIndex].models.forEach((m,i)=>{
        models.innerHTML += `
            <div class="vs-model ${i===vsModel?'active':''}" onclick="vsSelect(${i})">
                <div class="vs-model-thumb">
                    <img src="${m.image}">
                </div>
            </div>
        `;
    });
}

function vsPrev(){
    vsIndex = vsIndex===0 ? vsData.length-1 : vsIndex-1;
    vsModel = 0;
    renderVS();
}

function vsNext(){
    vsIndex = vsIndex===vsData.length-1 ? 0 : vsIndex+1;
    vsModel = 0;
    renderVS();
}

function vsSelect(i){
    vsModel=i;
    renderVS();
}

